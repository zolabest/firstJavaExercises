package exerciseOnVariables;

public class V1_VariableDeclaration {

	    public static void main(String[] args) {
	        int myInt = 10;
	        double myDouble = 5.5;
	        char myChar = 'A';

	        System.out.println("Integer: " + myInt);
	        System.out.println("Double: " + myDouble);
	        System.out.println("Character: " + myChar);
	    }
	}

