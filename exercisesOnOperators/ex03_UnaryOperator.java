package exercisesOnOperators;

public class ex03_UnaryOperator {

	    public static void main(String[] args) {
	        int a = 3;
	        int b = 5;

	        System.out.println("Pre-increment: " + (++a));
	        System.out.println("Post-increment: " + (b++));
	        System.out.println("Value after Post-increment: " + b);

	        int c = 7;
	        int d = 9;

	        System.out.println("Pre-decrement: " + (--c));
	        System.out.println("Post-decrement: " + (d--));
	        System.out.println("Value after Post-decrement: " + d);
	    }

	}

