package firstJava;

public class ex_1_DataTypes {

    public static void main(String[] args) {
        
        // 1. Primitive Data Types
        int num = 12_000;
        System.out.println("1. The INT data type value is: " + num);
        
        short sh = 12_345;
        System.out.println("2. The SHORT data type value is: " + sh);
        
        long lon = 347_659_272_536L;
        System.out.println("3. The LONG data type value is: " + lon);
        
        char ch = 'B';
        System.out.println("4. The CHAR data type value is: " + ch);
        
        boolean bool = true;
        System.out.println("5. The BOOLEAN data type value is: " + bool);
        
        float flo = 5_678.99f;
        System.out.println("6. The FLOAT data type value is: " + "$" + flo);
        
        double dou = 7_697_344.9456;
        System.out.println("7. The DOUBLE data type value is: " + "$" + dou);
        
        byte byt = 121;
        System.out.println("8. The BYTE data type value is: " + byt + "\n");
        
        
        
        
    }
}

